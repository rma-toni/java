package JavaBasics;

public class SwitchStatements {
    public static void main(String[] args) {
        int value = 0;
        switch (value){
            case 0:
                System.out.println("0");
                break;
            case 1:
                System.out.println("1");
                break;
            case 2:
                System.out.println("2");
                break;
            default:
                System.out.println("Default Value");
        }
    }
}
